import mongoose, { Schema } from "mongoose";

const userLoginSchema = mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    age: {
      type: Number,
      required: true,
    },
  },
  { timestamps: true }
);

export const UserDetails = mongoose.model("userdetail", userLoginSchema);
